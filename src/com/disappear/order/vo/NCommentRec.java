package com.disappear.order.vo;

public class NCommentRec {
	
	private String seq;
	private String nseq;
	private String nrseq;
	private String mid;
	private int nrecommend;
	
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getNseq() {
		return nseq;
	}
	public void setNseq(String nseq) {
		this.nseq = nseq;
	}
	public String getNrseq() {
		return nrseq;
	}
	public void setNrseq(String nrseq) {
		this.nrseq = nrseq;
	}
	public String getMid() {
		return mid;
	}
	public void setMid(String mid) {
		this.mid = mid;
	}
	public int getNrecommend() {
		return nrecommend;
	}
	public void setNrecommend(int nrecommend) {
		this.nrecommend = nrecommend;
	}
	
}
